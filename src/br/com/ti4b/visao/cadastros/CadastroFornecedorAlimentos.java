    public void getInfo() {
        if (fornecedor_Alimento.getRetorno().equals("Cadastrado com Sucesso!") || fornecedor_Alimento.getRetorno().equals("Alterado com Sucesso!")
                || fornecedor_Alimento.getRetorno().equals("Deletado com sucesso!")) {
            if (fornecedor_Alimento.getRetorno().equals("Alterado com Sucesso!")) {
                limpar_campos();
                info_error.setText("Alterado com Sucesso!");
            } else if (fornecedor_Alimento.getRetorno().equals("Cadastrado com Sucesso!")) {
                limpar_campos();
                info_error.setText("Cadastrado com Sucesso!");
            } else if (fornecedor_Alimento.getRetorno().equals("Deletado com Sucesso!")) {
                limpar_campos();
                info_error.setText("Deletado com Sucesso!");
            } else {
                info_error.setText(fornecedor_Alimento.getRetorno());
            }

        }
    }

    private void trataCampos(int tipo) {
        Component components[] = painelCadastro.getComponents();
        int o = 0;
        for (int i = 0; i < components.length; i++) {
            //Comparação com campoDeTexto
            if (components[i] instanceof JTextField) {
                if (((JTextField) components[i]).getText().isEmpty()) {
                    o++;
                }
            }
        }
        if (!validaCNPJ()) {
            o++;
        }
        if (o == 0) {
            if (tipo == 1 || tipo == 2) {
                cadastrar(tipo);
                getInfo();
            } else if (tipo == 3) {
                excluir();
            }
        } else {
            info_error.setText("Verifique todos os campos!");
        }
    }

    private void limpar_campos() {
        info_error.setText(null);
        bairroFornecedor.setText("");
        cidadeFornecedor.setText("");
        cnpjFornecedor.setText("");
        emailFornecedor.setText("");
        enderecoFornecedor.setText("");
        Numero.setText("");
        celularFornecedor.setText("");
        telefoneFornecedor.setText("");
        numeroLicitacao.setText("");
        nomeFormecedor.setText("");
        fornecedor_Alimento = new FornecedorAlimento();

    }

    private void cadastrar(int tipo) {

        fornecedor_Alimento.setNumero(Integer.parseInt(Numero.getText()));
        fornecedor_Alimento.setCidade(cidadeFornecedor.getText());
        fornecedor_Alimento.setCnpj(cnpjFornecedor.getText());
        fornecedor_Alimento.setBairro(bairroFornecedor.getText());
        fornecedor_Alimento.setEmail(emailFornecedor.getText());
        fornecedor_Alimento.setEndereco(enderecoFornecedor.getText());
        fornecedor_Alimento.setFone1(celularFornecedor.getText());
        fornecedor_Alimento.setFone2(telefoneFornecedor.getText());
        fornecedor_Alimento.setNome(nomeFormecedor.getText());
        fornecedor_Alimento.setNumero_licitacao(numeroLicitacao.getText());
        fornecedor_Alimento.setUf((String) ufFornecedor.getSelectedItem());
        FornecedorAlimentosDAO lvd = new FornecedorAlimentosDAO();

        lvd.Cadastrar(fornecedor_Alimento, tipo);

    }

    private void excluir() {
        funcoes_Fornecedor_Alimentos.excluirFornecedor(fornecedor_Alimento);
        if (fornecedor_Alimento.getRetorno().equals("Deletado com sucesso!")) {
            limpar_campos();
            info_error.setText("Deletado com sucesso!");

        }
    }

