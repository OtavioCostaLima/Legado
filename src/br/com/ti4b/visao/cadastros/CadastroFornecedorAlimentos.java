    public boolean validaCNPJ() {
        boolean ret;
        ValidaCNPJ validaCNPJ = new ValidaCNPJ();
        if (validaCNPJ.isCnpjValido(cnpjFornecedor.getText())) {
            ret = true;
            cnpjFornecedor.setForeground(Color.GREEN);
        } else {

            if (cnpjFornecedor.getText().equals("  .   .   /    -  ")) {
                ret = false;
                cnpjFornecedor.setForeground(Color.BLACK);
            } else {
                ret = false;
                cnpjFornecedor.setForeground(Color.red);
            }
        } // TODO add your handling code here:
        return ret; // TODO add your handling code here:
    }

