    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        if (m.usuarioLogado.isADM()) {
            if (fornecedor_Alimento.getCod_for_alimento() == 0) {
                trataCampos(1);
            } else {
                info_error.setText("Fornecedor já está cadastrado!");
            }
        }
    }//GEN-LAST:event_CadastrarActionPerformed

    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        if (m.usuarioLogado.isADM()) {
            if (fornecedor_Alimento.getCod_for_alimento() != 0) {
                trataCampos(2);
            } else {
                int i = JOptionPane.showConfirmDialog(null, "Os Campos estão vazios ou o Fornecedor não está Cadastrado! Deseja selecionar um Fornecedor? ", "Alerta", JOptionPane.YES_NO_OPTION);
                if (i == 0) {
                    SelecionaFornecedorAlimentos pesquisar_For_Ali = new SelecionaFornecedorAlimentos(this, m);
                    MenuPrincipal.jDesktopPane1.add(pesquisar_For_Ali);
                    pesquisar_For_Ali.setVisible(true);
                    pesquisar_For_Ali.setPosicao();
                }
            }
        }
    }//GEN-LAST:event_AlterarActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        if (m.usuarioLogado.isADM()) {
            if (fornecedor_Alimento.getCod_for_alimento() != 0) {
                FornecedorAlimentosDAO ffa = new FornecedorAlimentosDAO();
                ffa.excluirFornecedor(fornecedor_Alimento);
                if (fornecedor_Alimento.getRetorno().equals("Deletado com sucesso!")) {
                    limpar_campos();
                    info_error.setText("Deletado com sucesso!");
                }
                info_error.setText(fornecedor_Alimento.getRetorno());
            } else {
                int i = JOptionPane.showConfirmDialog(null, "Os Campos estão vazios ou Fornecedor não está Cadastrado!\nDeseja escolher um fornecedor? ", "INFORMAÇÃO", JOptionPane.YES_NO_OPTION);
                if (i == 0) {
                    SelecionaFornecedorAlimentos pesquisar_For_Ali = new SelecionaFornecedorAlimentos(this, m);
                    MenuPrincipal.jDesktopPane1.add(pesquisar_For_Ali);
                    pesquisar_For_Ali.setVisible(true);
                    pesquisar_For_Ali.setPosicao();
                }
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_ExcluirActionPerformed

